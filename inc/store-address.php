function storeAddress(){
	
	// Validation
	if(!$_POST['email']){ return "No email address provided"; } 

	if(!preg_match("/^[_a-z0-9-]+(\.[_a-z0-9-]+)*@[a-z0-9-]+(\.[a-z0-9-]+)*$/i", $_POST['email'])) {
		return "Email address is invalid"; 
	}

	require_once('MCAPI.class.php');
	// grab an API Key from http://admin.mailchimp.com/account/api/
	$api = new MCAPI('0f688b68dbb78733469959d35108da0d-us4');
	
	// grab your List's Unique Id by going to http://admin.mailchimp.com/lists/
	// Click the "settings" link for the list - the Unique Id is at the bottom of that page. 
	$list_id = "56963dcf3c";
	
	$merge_vars = array( 
		'NAME' => $_POST['name'],
		'EMAIL' => $_POST['email'],
		'WEBSITE' => $_POST['website'], 
		'FORMAT' => $_POST['format']
    );

	if($api->listSubscribe($list_id, $_POST['email'], $merge_vars, 'text') === true) {
		// It worked!	
		return 'Success! Check your e-mail to confirm sign up.';
	}else{
		// An error ocurred, return error message	
		return 'Error: ' . $api->errorMessage;
	}
	
}

// If being called via ajax, autorun the function
if($_POST['ajax']){ echo storeAddress(); }
